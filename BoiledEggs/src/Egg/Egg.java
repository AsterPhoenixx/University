package Egg;

public class Egg extends EggCoocking   {
	private String yalk;
	private String white;
	
	
	public Egg(String yalk, String white) {
		super();
		this.yalk = yalk;
		this.white = white;
	}
	public String getYalk() {
		return yalk;
	}
	public void setYalk(String yalk) {
		this.yalk = yalk;
	}
	public String getWhite() {
		return white;
	}
	public void setWhite(String white) {
		this.white = white;
	}
	
	



}
